#include "wolf3d.h"

/*
** **************************************************************************
**	void ft_fast_run(t_wolf3d *w, int state)
**	Function to speed up player
**	w->arr[9] == 1 ? ft_fast_run(w, 1) : ft_fast_run(w, 0);
**	NOT USED
** **************************************************************************
*/

void	ft_fast_run(t_wolf3d *w, int state)
{
	if (state)
		w->ms = 0.05;
	else
		w->ms = 0.03;
}