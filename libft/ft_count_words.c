#include "libft.h"

int	ft_count_words(char const *s, int i, char c)
{
	while (*s == c)
		s++;
	while (*s)
	{
		while (*s && *s != c)
			s++;
		while (*s == c)
			s++;
		i++;
	}
	return (i);
}
